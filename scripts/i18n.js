const ORIGINAL_TEXT = new WeakMap();

function normalizeText(value) {
  return value.replace(/\s+/g, " ").trim();
}

function translateNodeText(root, lang) {
  const walker = document.createTreeWalker(root, NodeFilter.SHOW_TEXT, {
    acceptNode(node) {
      const parent = node.parentElement;
      if (!parent || ["SCRIPT", "STYLE", "NOSCRIPT"].includes(parent.tagName)) {
        return NodeFilter.FILTER_REJECT;
      }
      return normalizeText(node.nodeValue) ? NodeFilter.FILTER_ACCEPT : NodeFilter.FILTER_REJECT;
    },
  });

  const nodes = [];
  while (walker.nextNode()) nodes.push(walker.currentNode);

  nodes.forEach((node) => {
    if (!ORIGINAL_TEXT.has(node)) ORIGINAL_TEXT.set(node, normalizeText(node.nodeValue));
    const source = ORIGINAL_TEXT.get(node);
    const translated = lang === "en" ? I18N[source] || I18N_LONG[source] : source;
    if (!translated) return;
    const leading = node.nodeValue.match(/^\s*/)?.[0] || "";
    const trailing = node.nodeValue.match(/\s*$/)?.[0] || "";
    node.nodeValue = `${leading}${translated}${trailing}`;
  });
}

function currentPageKey() {
  const path = window.location.pathname;
  if (path.endsWith("/") || path.endsWith("/index.html")) return "/";
  return path.split("/").pop();
}

function updateLangControls(lang) {
  document.querySelectorAll("[data-lang-option]").forEach((button) => {
    const active = button.dataset.langOption === lang;
    button.classList.toggle("is-active", active);
    button.setAttribute("aria-pressed", String(active));
  });
}

function readStoredLanguage() {
  try {
    return window.localStorage?.getItem("famecrypt-lang");
  } catch {
    return null;
  }
}

function writeStoredLanguage(lang) {
  try {
    window.localStorage?.setItem("famecrypt-lang", lang);
  } catch {
    // Language switching still works for the current page when storage is unavailable.
  }
}

function applyLanguage(lang) {
  const safeLang = lang === "en" ? "en" : "tr";
  document.documentElement.lang = safeLang;
  translateNodeText(document.body, safeLang);
  updateLangControls(safeLang);
  writeStoredLanguage(safeLang);

  const meta = I18N_PAGE_META[currentPageKey()]?.[safeLang];
  if (meta) {
    document.title = meta.title;
    const description = document.querySelector('meta[name="description"]');
    if (description) description.setAttribute("content", meta.description);
  }
}

function setupLanguageSwitcher() {
  document.querySelectorAll("[data-lang-option]").forEach((button) => {
    button.addEventListener("click", () => applyLanguage(button.dataset.langOption));
  });

  const initial = readStoredLanguage() || document.documentElement.lang || "tr";
  applyLanguage(initial);
}

document.addEventListener("DOMContentLoaded", setupLanguageSwitcher);
